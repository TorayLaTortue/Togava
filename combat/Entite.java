package combat;

public class Entite {
    private float action;
    private String nom;

    public Entite() {
        
    }

    public float getAction() {
        return action;
    }
    public void setAction(float action) {
        this.action = action;
    }
    public String getNom() {
        return nom;
    }
    public void setNom(String nom) {
        this.nom = nom;
    }
}
